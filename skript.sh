#!/bin/bash

# --- 1. ცვლადების განსაზღვრა (Variables) ---
# აქ ვქმნით "მისამართებს", რომ მერე ბევრჯერ არ ვწეროთ
ARHPATH="$HOME/Arh"        # საქაღალდე, სადაც არქივი (zip) შეინახება
EXTPATH="$HOME/newfolder"  # საქაღალდე, სადაც არქივს მერე ამოვალაგებთ
DATE=$(date +'%d-%m-%y')  # მიმდინარე თარიღი ფორმატით: დღე-თვე-წელი

echo "პროცესი დაიწყო: $DATE"

# --- 2. გარემოს მომზადება ---
# -p ნიშნავს: "შექმენი საქაღალდე, თუ ის არ არსებობს"
mkdir -p "$ARHPATH"

# --- 3. ციკლი საქაღალდეების და ფაილების შესაქმნელად ---
# i ცვლადი სათითაოდ მიიღებს მნიშვნელობებს 1-დან 5-მდე
for i in {1..5}
do
    # ვქმნით საქაღალდეს, მაგალითად: Dir1, Dir2...
    mkdir -p "Dir$i"
    
    # შიდა ციკლი: თითოეულ Dir-ში ვქმნით 5 ფაილს
    for j in {1..5}
    do
        # date ბრძანება იღებს დროს (საათი წუთი წამი)
        # > სიმბოლო ამ დროს წერს ფაილში (მაგ: Dir1/File-1.txt)
        date +'%H%M%S' > "Dir$i/File-$j.txt"
        
        # 1 წამით ვაჩერებთ, რომ ყოველ ფაილში განსხვავებული დრო ჩაიწეროს
        sleep 1
    done
    
    # ვაჩვენებთ ეკრანზე, რა ფაილები შეიქმნა ამ საქაღალდეში
    echo "შემოწმება Dir$i:"
    ls "Dir$i"
done

# --- 4. დაარქივება (Archiving) ---
# tar - ინსტრუმენტია
# -c (create): შექმენი
# -z (gzip): შეკუმშე
# -v (verbose): მაჩვენე პროცესი ეკრანზე
# -f (file): სახელი დაარქვი შემდეგი სიტყვა
# >> ArhList.txt : ფაილების სია ჩაწერე ამ ტექსტურ ფაილში
tar -czvf "$ARHPATH/Arh-$DATE.tar.gz" Dir* >> ArhList.txt

echo "არქივი შეიქმნა მისამართზე: $ARHPATH"

# --- 5. ამოარქივება (Extraction) ---
# ჯერ ვქმნით იმ ადგილს, სადაც უნდა ამოვალაგოთ
mkdir -p "$EXTPATH"

# -x (extract): ამოალაგე
# -C (change directory): ჯერ გადადი ამ საქაღალდეში და იქ ჩაყარე ფაილები
tar -xzvf "$ARHPATH/Arh-$DATE.tar.gz" -C "$EXTPATH"

echo "დასრულდა! ფაილები ამოარქივებულია აქ: $EXTPATH"

