#!/bin/bash

# მომხმარებლისგან ვიღებთ IP მისამართს ან საიტის სახელს (მაგ: google.com)
read -p "Введите адрес для пинга: " address

# ვიგებთ, რამდენჯერ უნდა გაიგზავნოს პაკეტი (მაგ: 4)
read -p "Введите количество запросов для проверки: " count

# ვასრულებთ პინგს და მთლიან პასუხს ვინახავთ ცვლადში 'ping_result'
ping_result=$(ping -c "$count" "$address")

# 'awk' ფილტრავს პასუხს: ეძებს ხაზს, რომელიც იწყება 'rtt'-ით 
# და ამოაქვს მე-5 სვეტი (საშუალო დრო) '/' გამყოფის მიხედვით
avg_ping=$(echo "$ping_result" | awk -F'/' '/^rtt/ { print $5 }')

# გამოგვაქვს საბოლოო შედეგი ეკრანზე
echo "Среднее время пинга до $address составляет: $avg_ping мс"

# შენიშვნა: macOS-ზე 'rtt'-ის ნაცვლად გამოიყენება 'round-trip'
